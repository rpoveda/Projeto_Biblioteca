    private void btnFiltroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltroActionPerformed
        Autor autor = new Autor();
        if (!txtCodigo.getText().equals("")) {
            autor.setCodigoAutor(Integer.parseInt(txtCodigo.getText()));
        }
        autor.setNomeAutor(txtNomeAutor.getText());
        autorController.setAutor(autor);
        
        initTable(autorController.select());
        
        autor = null;
        autorController.setAutor(null);
    }//GEN-LAST:event_btnFiltroActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Autor _autor = getAutorFromTable();
        
        if(_autor != null && JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o autor selecionado?") == JOptionPane.YES_OPTION){
            autorController.setAutor(_autor);
            if(autorController.delete()){
                initTable(autorController.selectAll());
            }else{
                JOptionPane.showMessageDialog(this, "Não foi possível excluir o autor selecionado.");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        Autor _autor = getAutorFromTable();
        
        if(_autor != null){
            AutorUI _autorUI = new AutorUI();
            
            _autorUI.setAutor(_autor);
            _autorUI.setVisible(true);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

