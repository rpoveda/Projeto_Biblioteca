    private void initTable(List<Autor> _lstAutor) {
        DefaultTableModel model = (DefaultTableModel) tblAutor.getModel();
        model.setRowCount(0);
        
        for(Autor autor : _lstAutor){
            model.addRow(new Object[]{autor.getCodigoAutor(), autor.getNomeAutor()});
            autor = null;
        }
        
        _lstAutor = null;
    }
    
    private Autor getAutorFromTable(){
        if(tblAutor.getSelectedRowCount() == 0){
            return null;
        }else{
            Autor _autor = new Autor();
            _autor.setCodigoAutor((Integer)tblAutor.getModel().getValueAt(tblAutor.getSelectedRow(), 0));
            _autor.setNomeAutor(String.valueOf(tblAutor.getModel().getValueAt(tblAutor.getSelectedRow(), 1)));
            
            return _autor;
        }
    }
    
